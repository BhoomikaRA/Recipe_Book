        // Enter cook mode
        function enterCookMode(recipeId) {
            const recipe = recipes.find(r => r.id === recipeId);
            
            if (!recipe) return;
            
            cookTitle.textContent = `${currentLang === 'en' ? 'Cooking: ' : 'ಬೇಯಿಸಲಾಗುತ್ತಿದೆ: '}${currentLang === 'en' ? recipe.name : (recipe.name_kn || recipe.name)}`;
            
            // Display ingredients
            cookIngredients.innerHTML = '';
            const ingredients = currentLang === 'en' ? recipe.ingredients : (recipe.ingredients_kn || recipe.ingredients);
            ingredients.forEach(ingredient => {
                const li = document.createElement('li');
                li.className = 'ingredient-item-cook';
                li.innerHTML = `
                    <span>${ingredient}</span>
                `;
                cookIngredients.appendChild(li);
            });
            
            // Display steps
            cookSteps.innerHTML = '';
            const steps = currentLang === 'en' ? recipe.steps : (recipe.steps_kn || recipe.steps);
            steps.forEach((step, index) => {
                const li = document.createElement('li');
                li.className = 'step-item-cook';
                li.innerHTML = `
                    <div class="step-number">${index + 1}</div>
                    <div class="step-text">${step}</div>
                `;
                
                // Add click to mark as complete
                li.addEventListener('click', function() {
                    this.classList.toggle('step-complete');
                });
                
                cookSteps.appendChild(li);
            });
            
            // Show cook mode and hide other content
            document.querySelector('.container').style.display = 'none';
            document.querySelector('footer').style.display = 'none';
            cookMode.style.display = 'block';
        }

