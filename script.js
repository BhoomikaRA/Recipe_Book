            const tags = document.getElementById('recipe-tags').value.split(',').map(tag => tag.trim());
            
            // Get ingredients
            const ingredients = [];
            document.querySelectorAll('#ingredient-list input').forEach(input => {
                if (input.value.trim()) {
                    ingredients.push(input.value.trim());
                }
            });
            
            // Get steps
            const steps = [];
            document.querySelectorAll('#step-list textarea').forEach(textarea => {
                if (textarea.value.trim()) {
                    steps.push(textarea.value.trim());
                }
            });
            
            // Validate form
            if (!name || !description || ingredients.length === 0 || steps.length === 0) {
                alert(currentLang === 'en' ? 'Please fill in all required fields' : 'ದಯವಿಟ್ಟು ಎಲ್ಲಾ ಅಗತ್ಯ ಕ್ಷೇತ್ರಗಳನ್ನು ಪೂರಿಸಿ');
                return;
            }
            
            // Create new recipe object
            const newRecipe = {
                id: Date.now(),
                name,
                name_kn: name, // In a real app, you would translate this
                image: image,
                description,
                description_kn: description, // In a real app, you would translate this
                category,
                prepTime: '20 min',
                cookTime: '30 min',
                difficulty: 'Medium',
                difficulty_kn: 'ಮಧ್ಯಮ',
                tags,
                ingredients,
                ingredients_kn: ingredients, // In a real app, you would translate these
                steps,
                steps_kn: steps // In a real app, you would translate these
            };
            
